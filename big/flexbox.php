What is Flexbox
 
“The Flexbox Layout officially called CSS Flexible Box Layout Module is new layout module in CSS3 made to improve the items align, directions and order in the container even when they are with dynamic or even unknown size.  It works exceptionally well with responsive design, and it is the current best option for vertical alignment when heights are unknown. Instead, you declare display: flex on the parent, and the child elements will conform accordingly according to additional properties that come along with it.” </p>

<p>When using Flexbox page content can be laid out in any direction (to the left, to the right, downwards or even upwards content can have their visual order reversed or rearranged the sizes of items can be flexed and making columns is easier. Even though using flexbox has its advantages and is the best page layout systems on the web today, it also has some performance issues under certain circumstances.  Sometimes content shifts around when loading, and display: flex shouldn’t be used for every container div. <p>There are also some issues with flexbox; IE compatibility; according to Taylor Lovett of  10up.com “The main idea behind the flex layout is to give the container the ability to alter its items’ width/height (and order) to best fill the available space (mostly to accommodate to all kind of display devices and screen sizes). A flex container expands items to fill available free space, or shrinks them to prevent overflow.” So that make flexbox not compatible with older versions.</p>
<p>Even though we’ve just scratched the surface of Flexbox, if you want a certain outcome and understand that it is not going to work with the certain old browsers, but works well in all modern browsers and if you know the specifics of you client needs and your project flexbox is good for different things</p>
 
