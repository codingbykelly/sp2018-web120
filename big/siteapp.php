<p>
In today’s ever changing technology dependent world it can be truly hard to decide what technological advances of to use to upgrade a big already established business or to start up a small business.  Of course mobile marketing will be one of the decisions that will have to be decided on, so you are trying to decide which method would be best to meet your needs. To begin we need to know the difference between responsive websites and native apps. So before making the final decision we need to know the basic pros and cons of the methods discussed.
According to Smashing Magazine, “Responsive Web design is the approach that suggests that design and development should respond to the user’s behavior and environment based on screen size, platform and orientation.”  Some of the major advantages of using responsive web design are:</p>
    
    <ul>
    <li>More Mobile Traffic</li>
    <li>Faster Mobile Development at Lower Costs</li>
    <li>Lower Maintenance Needs</li>
        <li>Higher Conversion Rates</li>
    <li>Faster Webpages</li>
    <li>Lower Bounce Rates</li>
        <li>Easier Analytics Reporting</li>
    <li>    Improved SEO</li>
</ul>
Some of the disadvantages are:
    
    <ul>
    <li>Slower Load Speed</li>
    <li>Respect of Usages</li>
    <li>Web Browser Compatibility</li>
</ul>
    

<p>According to Techopedia.com, “A native mobile app is a smartphone application that is coded in a specific programming language, such as Objective C for iOS or Java for Android operating systems.” Some of the advantages are:</p>
    <ul>
    <li>Push Notifications</li>
    <li>Higher Engagement for Users</li>
    <li>Payment Capabilities</li>
        <li>Quality User Experience</li>
    <li>Personilization</li>
    <li>Bluetooth and Other Use of Smartphone Hardware</li>
        <li>Better Behavioral Tracking</li>
    <li>    Improved SEO</li>
</ul>
Some of the disadvantages are:
    
    <ul>
    <li>Slower Load Speed</li>
    <li>Respect of Usages</li>
    <li>Web Browser Compatibility</li>
</ul>
    

Some of the disadvantages are:
    
    <ul>
    <li>More Expensive to Design and Develop</li>
    <li>Less Accessible to Search</li>
    
</ul>
    
<p>Just like any other technology each method has its ups and downs and it’s up to you how you are going to benefit from whichever method you choose.<p>
 
