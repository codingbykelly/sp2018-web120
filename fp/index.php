		<p>After living on my own in NY from 2005 until 2009, I decided one day to hop on a plane and move to Puerto Rico. I arrived at 12.15pm August 29, 2009.

 

I attempted to study accounting at the Inter-American University of Aguadilla. But  I was bored with some of the classes having taken them in High school. So I left the university and signed up for Beauty School.

 

I started My Angel Beauty School for nail technician in January of 2011 without being able to speak Spanish. I refused to learn before then.<p/>

<p>My first professor at beauty school did not speak English and although the school offered me English textbooks I refused and decided to learn Spanish. So while I was learning how to do nails I was also learning Spanish.

Beauty school was harder than you think. I had to learn so many things about the body and infections it took almost 6 months of information before we finally learned a basic manicure. And before I knew it we were practicing acrylics and gels learning hand designs and 3d art. This turned into me discovering a passion I never knew existed. In July 2012 I graduated high honor ( I aced every test and practice) as a certified Nail Technician with over 1000 hours under my belt. I am currently trying to become licensed in NY State.</p>

 

<p>I moved back to NY right before hurricane Sandy and soon after became a mom. Having my son turned me into a crazy planner and list person. I have tried too many planners. From cheap 99 cent store ones to really expensive ones. Right now I am using an Erin Condren life planner annual months.  There are so many things to learn when it comes to planning. Bullet planning  and sticker pages, pen styles and page thickness and more. I’m still learning new things. But my new favorite thing is sticker planning and making.</p>

 

<p>I also have been helping friends write resumes for years. This started in High School. So back then I was a part of this program called the academy of finance. We learned numerous things to help us get ahead in life and things we should know about business and finance. (hence my infatuation with accounting). After that I became everyone’s  go to girl for resumes.

 

Im also good at party planning and house keeping.</p>
		
